async function downloadSelectedPdf() {
    const selectedValues = Array.from(document.querySelectorAll('.report-selection:checked'))
        .map(checkbox => checkbox.value);

    if (selectedValues.length === 0) {
        alert('Please select at least one report or summary to download.');
        return;
    }

    // Separate selected ids into two buckets:
    // - generated report ids like "claim-0-question-1"
    // - summary ids like "claim-0-summary"
    const selectedReportIds = selectedValues.filter(id => !id.endsWith('-summary'));
    const selectedSummaryIds = selectedValues.filter(id => id.endsWith('-summary'));

    // Build inline summary_contents for any selected summary ids
    const summary_contents = selectedSummaryIds.map(id => {
        // id is "claim-<i>-summary"
        const claimIdx = parseInt(id.split('-')[1]);
        const claimText = document.querySelector(`#claim-${claimIdx} .claim-text`)?.textContent || `Claim ${claimIdx + 1}`;
        const modelVerdictEl = document.querySelector(`#model-verdict-claim-${claimIdx}`);
        const externalVerdictEl = document.querySelector(`#external-verdict-claim-${claimIdx}`);
        const modelHtml = modelVerdictEl ? modelVerdictEl.innerHTML : '';
        const externalHtml = externalVerdictEl ? externalVerdictEl.innerHTML : '';

        // Provide both text and sanitized-ish HTML for backend flexibility
        return {
            id,
            claimIdx,
            title: claimText,
            model_verdict_text: modelVerdictEl ? modelVerdictEl.textContent : '',
            external_verification_text: externalVerdictEl ? externalVerdictEl.textContent : '',
            // send innerHTML so backend can render formatted content into PDF if supported
            model_verdict_html: modelHtml,
            external_verification_html: externalHtml
        };
    });

    const payload = {
        selected_reports: selectedReportIds,
        summary_contents: summary_contents
    };

    toggleLoading(downloadPdfBtn, true, 'Generating PDF...');

    try {
        const response = await fetch('/export-pdf', {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify(payload)
        });

        if (!response.ok) {
            const text = await response.text();
            throw new Error(text || `PDF generation failed: ${response.status}`);
        }

        const blob = await response.blob();
        const url = window.URL.createObjectURL(blob);
        const a = document.createElement('a');
        a.href = url;
        a.download = 'SciCheck_AI_Report.pdf';
        document.body.appendChild(a);
        a.click();
        window.URL.revokeObjectURL(url);
        document.body.removeChild(a);
    } catch (error) {
        alert(`Error downloading PDF: ${error.message}`);
    } finally {
        toggleLoading(downloadPdfBtn, false);
    }
}
