function updatePdfSelectionList() {
    const previouslyChecked = new Set(
        Array.from(document.querySelectorAll('.report-selection:checked')).map(c => c.value)
    );

    pdfSelectionList.innerHTML = '';
    const foundItems = [];

    // 1) Add model-verdict + external-verification summaries per claim
    const allClaims = document.querySelectorAll('.claim-card');
    allClaims.forEach((claim, idx) => {
        // claim-card id is "claim-<i>"
        const claimIdx = idx;
        const claimText = claim.querySelector('.claim-text')?.textContent?.trim() || `Claim ${claimIdx + 1}`;
        const modelVerdictEl = claim.querySelector(`#model-verdict-claim-${claimIdx}`) || claim.querySelector(`#model-verdict-claim-${claimIdx}`);
        const externalVerdictEl = claim.querySelector(`#external-verdict-claim-${claimIdx}`) || claim.querySelector(`#external-verdict-claim-${claimIdx}`);
        const modelVerdict = modelVerdictEl ? modelVerdictEl.textContent.trim() : '';
        const externalVerdict = externalVerdictEl ? externalVerdictEl.textContent.trim() : '';

        const hasModel = modelVerdict && !modelVerdict.toLowerCase().includes('loading');
        const hasExternal = externalVerdict && !externalVerdict.toLowerCase().includes('loading') && !externalVerdict.toLowerCase().includes('toggled off');

        if (hasModel || hasExternal) {
            foundItems.push({
                id: `claim-${claimIdx}-summary`,
                claimIdx,
                questionIdx: null,
                claimText,
                questionText: 'Model verdict + external verification'
            });
        }
    });

    // 2) Add any generatedReports tracked in memory (safe single source)
    generatedReports.forEach(r => {
        if (!foundItems.find(f => f.id === r.id)) {
            foundItems.push({
                id: r.id,
                claimIdx: r.claimIdx,
                questionIdx: r.questionIdx,
                claimText: r.claimText,
                questionText: r.questionText
            });
        }
    });

    // 3) Also scan DOM for any report-claim-* containers that have content (covers streamed DOM-only cases)
    const allReportContainers = document.querySelectorAll('[id^="report-claim-"]');
    allReportContainers.forEach(container => {
        const reportBox = container.querySelector('.report-box');
        if (reportBox && reportBox.textContent.trim().length > 50) {
            const idParts = container.id.split('-'); // e.g. ["report","claim","0","question","1"]
            // safe parsing
            const claimIdx = parseInt(idParts[2]);
            const questionIdx = parseInt(idParts[4]);
            const id = container.id.replace('report-', '');
            if (!foundItems.find(f => f.id === id)) {
                const claimText = document.querySelector(`#claim-${claimIdx} .claim-text`)?.textContent || `Claim ${claimIdx + 1}`;
                const questionText = document.querySelector(`#questions-list-claim-${claimIdx} li:nth-child(${questionIdx + 1}) span`)?.textContent?.replace(/Q\d+:/, '').trim() || `Question ${questionIdx + 1}`;
                foundItems.push({
                    id,
                    claimIdx,
                    questionIdx,
                    claimText,
                    questionText
                });
            }
        }
    });

    // 4) Render the selection UI and preserve previous checks
    if (foundItems.length === 0) {
        pdfSelectionList.innerHTML = '<p class="text-muted">No analysis data yet. Run analysis first.</p>';
    } else {
        foundItems.forEach(item => {
            const div = document.createElement('div');
            div.className = 'form-check mb-2';
            const isChecked = previouslyChecked.has(item.id) || previouslyChecked.size === 0;
            div.innerHTML = `
                <input class="form-check-input report-selection" type="checkbox"
                    id="pdf-${item.id}" value="${item.id}" ${isChecked ? 'checked' : ''}>
                <label class="form-check-label" for="pdf-${item.id}">
                    <strong>Claim ${item.claimIdx + 1}:</strong> ${item.claimText.substring(0, 80)}${item.claimText.length > 80 ? '...' : ''}<br>
                    <small class="text-muted">${item.questionText}</small>
                </label>
            `;
            pdfSelectionList.appendChild(div);
        });
    }

    // Always show section
    pdfDownloadSection.classList.remove('d-none');
}
